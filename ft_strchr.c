#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	i;

	i = c;
	while (*s != '\0')
	{
		if (*s == i)
			return ((char *)s);
		s++;
	}
	if (i == '\0')
		return ((char *)s);
	return (NULL);
}
